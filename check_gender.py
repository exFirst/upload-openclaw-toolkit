#!/usr/bin/env python3
"""
Проверка текста на мужские окончания.
Запускать перед отправкой любого сообщения.
"""
import sys
import re

# Глаголы прошедшего времени — мужские окончания
MALE_PATTERNS = [
    # Одиночные глаголы: сделал, пошёл, нашёл
    r'\b(?:сделал|пошёл|нашёл|пришёл|ушёл|прошёл|зашёл|вышел|понял|принял|занял|поднял|снял|обнял)',
    r'\b(?:обновил|починил|настроил|запустил|остановил|установил|восстановил|сохранил|удалил)',
    r'\b(?:написал|сказал|рассказал|показал|прочитал|прочитала|перечитала|перечитал)',
    r'\b(?:добавил|убрал|поправил|исправил|отправил|получил|включил|выключил)',
    r'\b(?:решил|проверил|увидел|посмотрел|забыл|вспомнил|открыл|закрыл)',
    r'\b(?:создал|собрал|разобрал|переделал|доделал|сделал)',
    r'\b(?:помог|смог|лёг|берёг|стерёг|помешал)',
    r'\b(?:(?:по|за|на|вы|при|у|с|о|пере|до|про|под|от|об|в)глянул|глянул)',
    r'\b(?:был)',
    r'\b(?:взял)',
    r'\b(?:начал|начал[оа]?)',
]

# Возвратные мужские
MALE_REFLEXIVE = [
    r'\b(?:оказался|согласился|извинился|вернулся|остался|проснулся|очнулся|отозвался)',
    r'\b(?:занялся|взялся|принялся|улыбнулся|усмехнулся|оглянулся)',
    r'\b(?:надеялся|старался|боялся|смеялся|сомневался|колебался)',
]

# Контекстные проверки - "я" + мужской глагол
I_MALE = [
    r'\bЯ\s+\w+л\b(?!а)',
]

def check_text(text):
    """Проверяет текст на мужские окончания и возвращает ошибки."""
    errors = []
    
    for pattern in MALE_PATTERNS:
        matches = re.findall(pattern, text, re.IGNORECASE)
        for m in matches:
            # Проверяем, не стоит ли перед словом отрицание "не" с женским 
            # (это уже ок), или не часть ли это цитаты
            errors.append(f"❌ Мужской глагол: '{m}'")
    
    for pattern in MALE_REFLEXIVE:
        matches = re.findall(pattern, text, re.IGNORECASE)
        for m in matches:
            errors.append(f"❌ Мужской возвратный: '{m}'")
    
    return errors

